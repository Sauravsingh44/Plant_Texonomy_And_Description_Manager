import java.util.List;

public class Flower {
    private String commonName;
    private String scientificName;
    private String family;
    private String genus;
    private String height;
    private String leafShape;
    private String leafColor;
    private String flowerShape;
    private List<String> flowerColor;
    private String bloomingPeriod;
    private String soilType;
    private String waterRequirements;
    private String sunlightRequirements;
    private String nativeRange;
    private String distribution;
    private List<String> uses;

    // Getters and Setters
    public String getCommonName() {
        return commonName;
    }
    public void setCommonName(String commonName) {
        this.commonName = commonName;
    }

    public String getScientificName() {
        return scientificName;
    }
    public void setScientificName(String scientificName) {
        this.scientificName = scientificName;
    }

    public String getFamily() {
        return family;
    }
    public void setFamily(String family) {
        this.family = family;
    }

    public String getGenus() {
        return genus;
    }
    public void setGenus(String genus) {
        this.genus = genus;
    }

    public String getHeight() {
        return height;
    }
    public void setHeight(String height) {
        this.height = height;
    }

    public String getLeafShape() {
        return leafShape;
    }
    public void setLeafShape(String leafShape) {
        this.leafShape = leafShape;
    }

    public String getLeafColor() {
        return leafColor;
    }
    public void setLeafColor(String leafColor) {
        this.leafColor = leafColor;
    }

    public String getFlowerShape() {
        return flowerShape;
    }
    public void setFlowerShape(String flowerShape) {
        this.flowerShape = flowerShape;
    }

    public List<String> getFlowerColor() {
        return flowerColor;

    }
    public void setFlowerColor(List<String> flowerColor) {
        this.flowerColor = flowerColor;
    }

    public String getBloomingPeriod() {
        return bloomingPeriod;
    }
    public void setBloomingPeriod(String bloomingPeriod) {
        this.bloomingPeriod = bloomingPeriod;
    }

    public String getSoilType() {
        return soilType;
    }
    public void setSoilType(String soilType) {
        this.soilType = soilType;
    }

    public String getWaterRequirements() {
        return waterRequirements;
    }
    public void setWaterRequirements(String waterRequirements) {
        this.waterRequirements = waterRequirements;
    }

    public String getSunlightRequirements() {
        return sunlightRequirements;
    }
    public void setSunlightRequirements(String sunlightRequirements) {
        this.sunlightRequirements = sunlightRequirements;
    }

    public String getNativeRange() {
        return nativeRange;
    }
    public void setNativeRange(String nativeRange) {
        this.nativeRange = nativeRange;
    }

    public String getDistribution() {
        return distribution;
    }
    public void setDistribution(String distribution) {
        this.distribution = distribution;
    }

    public List<String> getUses() {
        return uses;
    }
    public void setUses(List<String> uses) {
        this.uses = uses;
    }
}
